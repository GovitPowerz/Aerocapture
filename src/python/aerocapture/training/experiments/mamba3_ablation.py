"""Mamba-3 ablation spike: 2x2 (discretization x state_mode) -> tail DV with sigma_run.

Isolates the two Mamba-3 axes -- exponential-trapezoidal discretization and
complex-diagonal (rotational) state -- on the aerocapture guidance task. Four
arms (baseline / trapz / complex / both) x N seed-repeats are PSO-trained at a
reduced budget, evaluated on ONE shared reserved pool, and reported with the
run-to-run std (sigma_run) so no inter-arm gap is claimed unless it clears
sigma_run (the project's recorded lesson: single-run deltas are noise).

The generated configs base-inherit the mission + common bases directly (NOT the
warm-start Mamba config): warm-start routes through V2Policy, which rejects
Mamba3 (PSO-only). full_neural + atan2_signed needs no scaffolding. seed_strategy
is "fixed" so all arms at repeat r see the identical training seeds -- a clean
controlled comparison; sigma_run comes from the 3 repeats + PSO stochasticity.

CLI (from repo root):
    python -m aerocapture.training.experiments.mamba3_ablation --generate --repeats 3
    python -m aerocapture.training.experiments.mamba3_ablation --train  --repeats 3 --n-gen 500 --training-n-sims 10
    python -m aerocapture.training.experiments.mamba3_ablation --eval --report --repeats 3 --n-sims 1000
    python -m aerocapture.training.experiments.mamba3_ablation --all --repeats 3 --n-gen 500 --n-sims 1000
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path
from typing import Any

import numpy as np

# arm -> (discretization, state_mode)
ARMS: dict[str, tuple[str, str]] = {
    "baseline": ("euler", "real"),
    "trapz": ("trapezoidal", "real"),
    "complex": ("euler", "complex"),
    "both": ("trapezoidal", "complex"),
}

BASE_SEED = 20260707
CONFIG_DIR = Path("configs/training/mamba3")
OUT_DIR = Path("training_output/mamba3")
INPUT_MASK = list(range(21))  # 21 orbital/aero inputs (matches the base Mamba config)


def _cvar95(dv: np.ndarray) -> float:
    """CVaR95 = mean of the worst 5% (DV at or above the 95th percentile).

    The propellant-sizing tail statistic: what the ergol budget must cover in the
    bad-luck cases, not the median mission.
    """
    if dv.size == 0:
        return float("nan")
    thr = float(np.percentile(dv, 95))
    tail = dv[dv >= thr]
    return float(np.mean(tail)) if tail.size else thr


def _leaf_toml(arm: str, disc: str, sm: str, seed: int, out_dir: Path, n_gen: int, training_n_sims: int) -> str:
    mask = ", ".join(str(i) for i in INPUT_MASK)
    return f"""# Auto-generated by mamba3_ablation.py -- do not edit by hand.
# Arm: {arm} (discretization={disc}, state_mode={sm}), seed={seed}.
base = ["../../missions/mars.toml", "../common.toml"]

[guidance]
type = "neural_network"

[data]
neural_network = "{out_dir.as_posix()}/best_model.json"
results_suffix = ".mamba3_{arm}_s{seed - BASE_SEED}"

[network]
input_mask = [{mask}]

[[network.architecture]]
type = "dense"
input_size = 21
output_size = 32
activation = "swish"

[[network.architecture]]
type = "mamba3"
input_size = 32
d_state = 16
discretization = "{disc}"
state_mode = "{sm}"

[[network.architecture]]
type = "dense"
input_size = 32
output_size = 2
activation = "asinh"

[optimizer]
algorithm = "pso"
n_pop = 64
n_gen = {n_gen}
seed_strategy = "fixed"
training_n_sims = {training_n_sims}
validation_n_sims = 200

[monte_carlo]
seed = {seed}
"""


def generate_configs(repeats: int, n_gen: int, training_n_sims: int) -> None:
    CONFIG_DIR.mkdir(parents=True, exist_ok=True)
    for arm, (disc, sm) in ARMS.items():
        for r in range(repeats):
            out_dir = OUT_DIR / f"{arm}_s{r}"
            path = CONFIG_DIR / f"{arm}_s{r}.toml"
            path.write_text(_leaf_toml(arm, disc, sm, BASE_SEED + r, out_dir, n_gen, training_n_sims))
    print(f"Wrote {len(ARMS) * repeats} arm configs to {CONFIG_DIR}/")


def train(repeats: int, n_gen: int, training_n_sims: int, sim_timeout: float | None, force: bool, from_scratch: bool) -> None:
    jobs = [(arm, r) for arm in ARMS for r in range(repeats)]
    for i, (arm, r) in enumerate(jobs, 1):
        out_dir = OUT_DIR / f"{arm}_s{r}"
        config = CONFIG_DIR / f"{arm}_s{r}.toml"
        if (out_dir / "best_model.json").exists() and not force and not from_scratch:
            print(f"[{i}/{len(jobs)}] skip {arm}_s{r} (best_model.json exists; --force/--from-scratch to retrain)")
            continue
        cmd = [
            sys.executable,
            "-m",
            "aerocapture.training.train",
            str(config),
            "--n-gen",
            str(n_gen),
            "--no-tui",
            "--skip-report",
            "--training-n-sims",
            str(training_n_sims),
            "--output-dir",
            str(out_dir),
        ]
        if from_scratch:
            cmd.append("--from-scratch")
        if sim_timeout is not None:
            cmd += ["--sim-timeout", str(sim_timeout)]
        print(f"[{i}/{len(jobs)}] train {arm}_s{r}: {' '.join(cmd)}")
        rc = subprocess.run(cmd).returncode
        if rc != 0:
            print(f"  WARNING: training exited {rc} for {config}")


def _score_one(config: Path, model: Path, seeds: list[int], cost_kwargs: dict[str, Any], sim_timeout: float | None) -> dict[str, float]:
    import aerocapture_rs

    from aerocapture.training import charts
    from aerocapture.training.report import compute_eval_summary

    overrides = [{"simulation.n_sims": 1, "data.neural_network": str(model), "monte_carlo.seed": int(s)} for s in seeds]
    batch = aerocapture_rs.run_batch(str(config), overrides, n_threads=None, include_trajectories=False, sim_timeout_secs=sim_timeout)
    final = np.array(batch.final_records, dtype=np.float64)
    summary = compute_eval_summary(final, n_sims=len(seeds), cost_kwargs=cost_kwargs)
    captured = charts.is_captured(final)
    dv = np.clip(final[captured, charts._FR_DV_TOTAL], charts.DV_FLOOR, charts.DV_CAP)
    return {
        "rms_cost": float(summary["cost"]["rms"]),
        "capture_rate": float(summary["capture_rate"]),
        "dv_p50": float(summary["captured"]["dv"]["p50"]) if summary["captured"] else float("nan"),
        "dv_p95": float(summary["captured"]["dv"]["p95"]) if summary["captured"] else float("nan"),
        "cvar95": _cvar95(dv),
    }


def eval_arms(repeats: int, n_sims: int, sim_timeout: float | None) -> dict[str, Any]:
    from aerocapture.training.evaluate import MAMBA3_EVAL_SEED_OFFSET, make_reserved_seeds
    from aerocapture.training.report import read_cost_kwargs

    seeds = make_reserved_seeds(0, MAMBA3_EVAL_SEED_OFFSET, n_sims)
    out: dict[str, Any] = {"n_sims": n_sims, "repeats": repeats, "arms": {}}
    for arm in ARMS:
        per_rep: list[dict[str, float]] = []
        for r in range(repeats):
            out_dir = OUT_DIR / f"{arm}_s{r}"
            model = out_dir / "best_model.json"
            config = CONFIG_DIR / f"{arm}_s{r}.toml"
            if not model.exists():
                print(f"  skip {arm}_s{r} (untrained: no {model})")
                continue
            per_rep.append(_score_one(config, model, seeds, read_cost_kwargs(config), sim_timeout))
        out["arms"][arm] = _aggregate(per_rep)
    (OUT_DIR / "ablation_results.json").parent.mkdir(parents=True, exist_ok=True)
    (OUT_DIR / "ablation_results.json").write_text(json.dumps(out, indent=2))
    print(f"Wrote eval results to {OUT_DIR / 'ablation_results.json'}")
    return out


def _aggregate(per_rep: list[dict[str, float]]) -> dict[str, Any]:
    if not per_rep:
        return {"n_repeats": 0}
    keys = ("rms_cost", "capture_rate", "dv_p50", "dv_p95", "cvar95")
    agg: dict[str, Any] = {"n_repeats": len(per_rep), "per_repeat": per_rep}
    for k in keys:
        vals = np.array([d[k] for d in per_rep], dtype=np.float64)
        agg[k] = {"mean": float(np.nanmean(vals)), "std": float(np.nanstd(vals))}
    return agg


def report(results: dict[str, Any]) -> None:
    arms = results["arms"]
    print("\n" + "=" * 78)
    print(f"Mamba-3 ablation -- {results['repeats']} repeats x {results['n_sims']} eval sims/arm")
    print("Tail metrics are the sizing statistics; lead with dv_p95 / CVaR95, not p50.")
    print("=" * 78)
    header = f"{'arm':10s} {'cap%':>6s} {'rms':>9s} {'dvP50':>8s} {'dvP95 +- sig':>18s} {'CVaR95 +- sig':>18s}"
    print(header)
    for arm in ARMS:
        a = arms.get(arm, {})
        if a.get("n_repeats", 0) == 0:
            print(f"{arm:10s} {'(untrained)':>60s}")
            continue
        cap = a["capture_rate"]["mean"] * 100
        rms = a["rms_cost"]["mean"]
        p50 = a["dv_p50"]["mean"]
        p95, p95s = a["dv_p95"]["mean"], a["dv_p95"]["std"]
        cv, cvs = a["cvar95"]["mean"], a["cvar95"]["std"]
        print(f"{arm:10s} {cap:6.1f} {rms:9.1f} {p50:8.1f} {p95:10.1f} +-{p95s:5.1f} {cv:10.1f} +-{cvs:5.1f}")

    base = arms.get("baseline", {})
    if base.get("n_repeats", 0) == 0:
        print("\nNo baseline arm trained -- cannot compute significance.")
        return
    min_reps = min((arms[a].get("n_repeats", 0) for a in ARMS if arms.get(a, {}).get("n_repeats", 0) > 0), default=0)
    if min_reps < 2:
        print(f"\nSignificance skipped: need >=2 repeats to estimate sigma_run (got min {min_reps}). Re-run with --repeats 3+.")
        return
    print("\nSignificance vs baseline (gap clears sigma_run only if |gap| > combined std):")
    for metric in ("dv_p95", "cvar95"):
        bmean, bstd = base[metric]["mean"], base[metric]["std"]
        print(f"  [{metric}]  baseline = {bmean:.1f} +- {bstd:.1f}")
        for arm in ("trapz", "complex", "both"):
            a = arms.get(arm, {})
            if a.get("n_repeats", 0) == 0:
                continue
            gap = a[metric]["mean"] - bmean
            sig = float(np.sqrt(bstd**2 + a[metric]["std"] ** 2))
            verdict = "SIGNIFICANT" if abs(gap) > sig else "within sigma_run (noise)"
            arrow = "better" if gap < 0 else "worse"
            print(f"    {arm:10s} gap = {gap:+7.1f} ({arrow}), sigma_run = {sig:5.1f}  -> {verdict}")


def main() -> None:
    p = argparse.ArgumentParser(description="Mamba-3 2x2 ablation spike")
    p.add_argument("--generate", action="store_true", help="write arm configs")
    p.add_argument("--train", action="store_true", help="PSO-train each arm x repeat (subprocess)")
    p.add_argument("--eval", action="store_true", help="score deployed models on the reserved pool")
    p.add_argument("--report", action="store_true", help="print the arm comparison table + significance")
    p.add_argument("--all", action="store_true", help="generate -> train -> eval -> report")
    p.add_argument("--repeats", type=int, default=3, help="seed-repeats per arm (sigma_run sample)")
    p.add_argument("--n-gen", type=int, default=500, help="PSO generations per training run")
    p.add_argument("--training-n-sims", type=int, default=10, help="sims per individual per generation")
    p.add_argument("--n-sims", type=int, default=1000, help="reserved eval pool size")
    p.add_argument("--sim-timeout", type=float, default=None, help="per-sim wall-clock timeout (s)")
    p.add_argument("--force", action="store_true", help="retrain even if best_model.json exists")
    p.add_argument("--from-scratch", action="store_true", help="wipe checkpoints + retrain")
    args = p.parse_args()

    if not any((args.generate, args.train, args.eval, args.report, args.all)):
        p.error("pass at least one of --generate/--train/--eval/--report/--all")

    if args.generate or args.all:
        generate_configs(args.repeats, args.n_gen, args.training_n_sims)
    if args.train or args.all:
        train(args.repeats, args.n_gen, args.training_n_sims, args.sim_timeout, args.force, args.from_scratch)
    results: dict[str, Any] | None = None
    if args.eval or args.all:
        results = eval_arms(args.repeats, args.n_sims, args.sim_timeout)
    if args.report or args.all:
        if results is None:
            results = json.loads((OUT_DIR / "ablation_results.json").read_text())
        report(results)


if __name__ == "__main__":
    main()
